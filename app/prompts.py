def build_user_message(article: dict, content: str, source_scope: str) -> str:
    """בונה את הודעת המשתמש עם המטא-דאטה והטקסט (מלא או תקציר)."""
    meta = (
        f"כותרת: {article.get('title','')}\n"
        f"עיתון: {article.get('journal','')} | שנה: {(article.get('pub_date') or '')[:4]}\n"
        f"סוגי פרסום (PubMed): {', '.join(article.get('pub_types', [])) or 'לא דווח'}\n"
        f"MeSH: {', '.join(article.get('mesh', [])[:15]) or 'לא דווח'}\n"
        f"DOI: {article.get('doi','לא דווח')}\n"
        f"SOURCE_SCOPE: {source_scope}\n"
    )
    return (
        f"{meta}\n"
        f"===== טקסט המאמר ({'טקסט מלא' if source_scope=='FULL_TEXT' else 'תקציר בלבד'}) =====\n"
        f"{content}\n"
        f"===== סוף הטקסט =====\n\n"
        f"בצע את הניתוח המלא והחזר JSON תקין יחיד בלבד."
    )
